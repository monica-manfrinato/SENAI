print("Me dê qual a distância que será percorrida e sua velocidade média que te darei o tempo de viagem final")
distancia = float(input("Qual a distância total até o seu destino? (Em Km)"))
vel_media = float(input("Qual a velocidade média durante o percurso (Em Km/h)"))
tempo = distancia/vel_media
print("A sua viagem levará",tempo,"horas!")
                  
